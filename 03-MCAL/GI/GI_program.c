void GI_voidEnable (void)
{
	SET_BIT(GI_u8_SREG_REG , GI_u8_SREG_I_BIT);
}

void GI_voidDisable(void)
{
	CLR_BIT(GI_u8_SREG_REG , GI_u8_SREG_I_BIT);
}
